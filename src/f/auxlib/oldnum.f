	SUBROUTINE OLDNUM(X,Y,HGHT,Z,T,N,IPF)
C
C	PLOTS A FLOATING POINT NUMBER.  USES INTEGER DIGITS SPECIFICATION.
C	DOES NOT RETURN PLOT LENGTH.
C
C	X,Y	(R)	COORDINATES OF STRING
C	HGHT	(R)	HEIGHT OF THE PLOTTED NUMBER IN FLT. PT.
C	Z	(R)	FLT. PT. NUMBER TO BE PLOTTED
C	T	(R)	ORIENTATION ANGLE FOR THE NUMBER
C	N	(I)	NUMBER OF DECIMAL DIGITS FOR OUTPUT.
C			N=-1 WILL SUPPRESS THE DECIMAL POINT.
C	IPF	(I)	<0 X,Y ARE LOWER LEFT CORNER
C			=0 X,Y ARE CENTER
C			>0 X,Y ARE LOWER RIGHT CORNER
C
	DATA KZERO/48/
	TPI=3.141592654/180.
	FPN=ABS(Z)*1.000002
	XT=X
	YT=Y
	ACNT=0.
	IPF2=IPF
	IF (IPF2) 1,2,2
2	IF (Z) 4,5,6
5	ACNT=2.+FLOAT(N)
	GOTO 3
4	ACNT=1.
6	IF (ABS(Z)-1.)5,11,11
11	ACNT=AINT(ALOG10(ABS(Z)))+FLOAT(N+1)+ACNT
3	CO=COS(TPI*T)
	SI=SIN(TPI*T)
	X0=0.
	Y0=0.
	IF (IPF2)1,7,8
8	X0=-ACNT*HGHT
	GOTO 9
7	X0=-ACNT*HGHT/2.
	Y0=-HGHT/2.
9	XT=X+X0*CO-Y0*CO
	YT=Y+Y0*SI+X0*SI
1	IF(Z)10,90,20
  10	CALL SYMBOL(XT,YT,HGHT,'-',T,1,-1)
	XT=999.
	YT=999.
  20	I=int(ALOG10(FPN)+1.0)
	IF (I) 50,50,30
  30	DO 40 J = 1,I
	K = int(FPN*10.0**(J-I))
	CALL SYMBOL(XT,YT,HGHT,CHAR(K+KZERO),T,-1,-1)
	XT=999.
	YT=999.
	FPN = FPN - FLOAT(K*10**(I-J))
  40	CONTINUE
	IF (N+1)80,80,50
  50	CALL SYMBOL(XT,YT,HGHT,'.',T,1,-1)
	XT=999.
	YT=999.
	IF(N)80,80,60
  60	DO 70 IJ=1,N
	K=int(FPN*10.0)
	CALL SYMBOL(XT,YT,HGHT,CHAR(K+KZERO),T,-1,-1)
  70	FPN=FPN*10.0-FLOAT(K)
  80	RETURN
  90	CALL SYMBOL(XT,YT,HGHT,'0.0000000000 ',T,2+N,-1)
	RETURN
	END
