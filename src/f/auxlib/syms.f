C			= -1 (x,y) to be lower left corner of plotted string
C                            (x and y not altered)
C			= 0  (x,y) to be center of plotted string
C                            (x and y not altered)
C                       = 1  (x,y) to be lower right corner of plotted string
C                            (x and y not altered)
C
C SYMS plots an ASCII string contained in a CHARACTER array.  SYMS provides
C additional math and plotting symbols as well as several character fonts
C (including Greek characters) and complicated equations typesetting.
C All but the 9th font have variable width characters.  Several fonts are
C designed to appear as solid characters when the plotting scale is small.
C
C The ASCII character "\" (decimal 92) is used as a control character to
C change fonts or subscripting options.  The character following the
C "\" character is used according to the following table:
C
C		SYMS Options
C
C  ASCII Character       Decimal	Effect
C ________________ 	_________      ________
C
C     0-9		  48-57		Change to font (0-9)
C     ; or (space)	 59 or 32	Move forward one standard space
C      :		   58		Move forward 1/2 standard space
C      ,		   44		Move forward 1/4 standard space
C      |		  124		Move backward one standard space
C      =		   61		Move backward 1/2 standard space
C      !		   33		Move backward 1/4 standard space
C      @		   64		Reset font, scale, etc. to default values
C      _		   95		Begin subscripting
C      ]		   93		Back up last character
C      ^		   94		Begin superscripting
C      [		   91		Reset current level of sub/super script
C      L		   76		Move lower 1/2 line
C      R		   82		Move higher 1/2 line
C      `		   96		Change scale (if next character is
C					 "+" increase scale size by 2
C					 "-" decrease scale size by 2)
C      O		   79		Begin over printing
C      U		   85		Begin under printing
C
C					Other characters are no-ops
C
C The width of one space is moved when moving forward or backward.
C Back up returns the positioning to the start of the last character.
C Up to 6 back up commands can be issued.  Super/sub scripting can be
C done recursively.  Only one level is "popped off" by the reset current 
C level of sub/super script.  Scale changes require an additional character
C (either a "+" or "-" to indicate the direction).  Over/under printing
C permit summation and integral limits to be added.  Fonts are described
C in the following Table:
C
C		Fonts Available in SYMS
C
C   Font		Characters		 Description
C  ______	       _____________		_____________
C
C    - [all]		ASCII 0-31		Plotting Symbols
C    0 [default]	ASCII 32-127		Simplex font--variable width
C    1			ASCII 32-127		Special math symbols
C    2			ASCII 32-127		Simplex Italic
C    3			ASCII 32-127		Roman
C    4			ASCII 32-127		Roman Italic
C    5			ASCII 32-127		Duplex
C    6			ASCII 32-127		Simplex Greek
C    7			ASCII 32-127		Complex Greek
C    8			ASCII 32-127		Crude Simplex--fixed width
C   		
C For example, the following summation using greek characters, a
C math symbol, and a super scripted variable can be wrtten:
C
C  alph string = \2A\0=\7R\6a\^2\[\]\_\6n\[\]\]\]\=\U\6n=1\@\]\]\O\1K
C
C	       infinity    2
C	    A = SIGMA alpha
C	       zeta=1     zeta
C
C Original font data is an extensive adaptation of the fonts originally
C developed by N.M. Wolcott at NBS. (See NBS special pub. 500-32).
C
