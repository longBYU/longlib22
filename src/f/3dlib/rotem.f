	SUBROUTINE ROTEM(IROT,ANG,R)
C
C	CREATED: DGL 1-AUG-1984
C
C	CREATES 3 AXIS ROTATION MATRIX
C
C	R	RESULT MATRIX R(4,4)
C	IROT	AXIS OF ROTATION
C		= 1  X AXIS
C		= 2  Y AXIS
C		= 3  Z AXIS
C	ANG	ANGLE OF ROTATION IN DEGREES
C
	DIMENSION R(4,4)
	X=ANG*3.141592654/180.
	IF (ANG.EQ.0.0) THEN
		C=1.0
		S=0.0
	ELSE IF (ABS(ANG).EQ.90.0) THEN
		C=0.0
		S=1.0*SIGN(1.,ANG)
	ELSE
		C=COS(X)
		S=SIN(X)
	ENDIF
	DO 10 I=1,4
		DO 20 J=1,4
			R(I,J)=0.
 20		CONTINUE
 10	CONTINUE
	IF (IROT .NE. 1) GO TO 200
	R(2,2)=C
	R(3,2)=-S
	R(2,3)=S
	R(3,3)=C
	R(1,1)=1.
	R(4,4)=1.
	RETURN
 200	IF (IROT .NE. 2) GO TO 300
	R(1,1)=C
	R(3,1)=S
	R(1,3)=-S
	R(3,3)=C
	R(2,2)=1.
	R(4,4)=1.
	RETURN
 300	R(1,1)=C
	R(2,1)=-S
	R(1,2)=S
	R(2,2)=C
	R(3,3)=1.
	R(4,4)=1.
	RETURN
	END
