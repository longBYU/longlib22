	INTEGER FUNCTION CLIP3D(X,Y,Z)
C
C	CREATED: DGL 1-AUG-1984
C
C	RETURNS A CODE INDICATING THE LOCATION OF THE POINT (X,Y,Z)
C	IN VIEWING PYRAMID
C
C		1001 | 1000 | 1010
C		------------------
C		0001 | 0000 | 0010
C		------------------
C		0101 | 0100 | 0100
C
	INTEGER C
	C=0
	IF (X.LT.-Z) THEN
		C=1
	ELSE 
		IF (X.GT.Z) C=2
	ENDIF
	IF (Y.LT.-Z) THEN
		C=IOR(C,4)
	ELSE
		IF (Y.GT.Z) C=IOR(C,8)
	ENDIF
	CLIP3D=C
	RETURN
	END
